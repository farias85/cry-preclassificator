    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(javax.swing.JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filtrofile = new FileNameExtensionFilter("*.wav", "wav");

        fileChooser.setFileFilter(filtrofile);
        int select = fileChooser.showOpenDialog(this);

        if (select == JFileChooser.APPROVE_OPTION) {
            File fichero = fileChooser.getSelectedFile();
            if (fichero.isFile()) {
                dirFile = fichero.getAbsolutePath();
                objWr.openWav(dirFile);

                left = objWr.getLeft();
                right = objWr.getRight();

                objChart.dibujarSenal(left, 3);
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

