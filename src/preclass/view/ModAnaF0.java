    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        AudioSource reader = null;
        try {
            reader = new AudioFileReader(dirFile, true);
            reader.setPreEmphasis(false, 1);

        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(ModAnaF0.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ModAnaF0.class.getName()).log(Level.SEVERE, null, ex);
        }

        de.fau.cs.jstk.sampled.AudioPlay play;
        try {
            play = new de.fau.cs.jstk.sampled.AudioPlay(reader);

            // play whole file
            while (play.write() > 0);

        } catch (IOException ex) {
            Logger.getLogger(ModAnaF0.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(ModAnaF0.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

